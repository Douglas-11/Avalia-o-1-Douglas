    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String nome = campoNome.getText();
        String cargo = campoCargo.getText();
        String cpf = campoCpf.getText();
        String telefone = campoTelefone.getText();
        String contabancaria = campoContaBancaria.getText();
        
        if (this.funcionario != null){
            this.funcionario.setNome(nome);
            this.funcionario.setCargo(cargo);
            this.funcionario.setCpf(cpf);
            this.funcionario.setTelefone(telefone);
            this.funcionario.setContabancaria(contabancaria);
            
            RepositorioDadosFuncionario.editarFuncionario(this.funcionario);
            
            JOptionPane.showMessageDialog(this, "Atualizado com sucesso!");
        }
        
        Funcionario funcionarioAtual = new Funcionario (nome, cargo, cpf, telefone, contabancaria);
        
        funcionarioAtual.imprimirDados();
        
        JOptionPane.showMessageDialog(this, funcionarioAtual);
        
        RepositorioDadosFuncionario.adicionarFuncionario(funcionarioAtual);
        this.dispose();                 
    }//GEN-LAST:event_jButton1ActionPerformed

